package txtlaskin;

public class Laskin {

    static int summa(int luku, int luku2) {
        int tulos = luku + luku2;

        return tulos;
    }

    static int vahennys(int luku, int luku2) {
        int tulos = luku - luku2;

        return tulos;
    }

    static int kerto(int luku, int luku2) {
        int tulos = luku * luku2;

        return tulos;
    }

    static double jako(int luku, int luku2) {
        double tulos = luku / (double)luku2;

        return tulos;
    }
}
